import json


def divideSet(X, Y):
    length_dataset = len(X)

    trainingfileX = open('./dataSet/trainingSetX.json', 'w')
    trainingfileY = open('./dataSet/trainingSetY.json', 'w')
    json.dump(X[0:3000],trainingfileX)
    json.dump(Y[0:3000],trainingfileY)


    cvfileX = open('./dataSet/crossValidationX.json', 'w')
    cvfileY = open('./dataSet/crossValidationY.json', 'w')
    json.dump(X[3000:3600],cvfileX)
    json.dump(Y[3000:3600],cvfileY)


    testfileX = open('./dataSet/testX.json', 'w')
    testfileY = open('./dataSet/testY.json', 'w')
    json.dump(X[3600:length_dataset],testfileX)
    json.dump(Y[3600:length_dataset],testfileY)
    return


